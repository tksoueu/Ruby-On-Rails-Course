# Snake Case é quando tudo é minúsculo e as palavras são separadas por _
# usamos snake case para nome de métodos e variáveis
this_is_snake_case
and_this_is_snake_case_too
def my_name_is
  #
end

# Camel Case é quando as palavras são todas juntas e se iniciam em letras maiúsculas
# usamos camel case principalmente para nomear classes
ThisIsCamelCase
AndThisIsCamelCaseToo

class SomeClass
  #
end

# para nomes de classes, o camel case tem que ser sempre com a primeira letra maiúscula, porém
# não necessariamente quando se usa camel case tem que a primeira palavra ser iniciada em maiúscula
thisIsAlsoCamelCase
